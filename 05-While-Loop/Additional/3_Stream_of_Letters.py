# --- 1. Инициализация на променливите ---

# Флагове, които следят дали сме срещнали c, o, и n поне веднъж
seen_c = False
seen_o = False
seen_n = False

# Променлива за натрупване на буквите от текущата дума
current_word = ""

# --- 2. Цикъл за четене на вход ---

# Използваме безкраен цикъл, който ще прекратим с break
while True:
    # Прочитаме поредния символ от входа
    char = input()

    # --- 3. Проверка за край ---
    if char == "End":
        break  # Ако командата е "End", прекратяваме цикъла

    # --- 4. Проверка дали символът е буква ---
    # Използваме .isalpha(), за да проверим дали е латинска буква
    if char.isalpha():

        # --- 5. Логика за тайната команда (c, o, n) ---

        # Проверка за 'c'
        if char == 'c':
            if not seen_c:
                # Ако виждаме 'c' за първи път,
                # вдигаме флага и *не* добавяме буквата към думата
                seen_c = True
            else:
                # Ако вече сме виждали 'c', добавяме го към думата
                current_word += char

        # Проверка за 'o'
        elif char == 'o':
            if not seen_o:
                # Ако виждаме 'o' за първи път,
                # вдигаме флага и *не* добавяме буквата
                seen_o = True
            else:
                # Ако вече сме виждали 'o', добавяме го
                current_word += char

        # Проверка за 'n'
        elif char == 'n':
            if not seen_n:
                # Ако виждаме 'n' за първи път,
                # вдигаме флага и *не* добавяме буквата
                seen_n = True
            else:
                # Ако вече сме виждали 'n', добавяме го
                current_word += char

        # --- 6. Обработка на всички останали букви ---
        else:
            # Ако буквата не е 'c', 'o' или 'n',
            # просто я добавяме към текущата дума
            current_word += char

        # --- 7. Проверка за край на думата ---
        # След като сме обработили буквата, проверяваме дали
        # и трите флага вече са вдигнати (True)
        if seen_c and seen_o and seen_n:
            # Ако да, отпечатваме събраната дума, последвана от интервал
            # (end=" " гарантира, че всичко остава на един ред)
            print(current_word, end=" ")

            # Нулираме всичко, за да започнем да събираме нова дума
            current_word = ""
            seen_c = False
            seen_o = False
            seen_n = False

    # --- 8. Игнориране на символи ---
    # Ако char не е .isalpha() (т.е. е '!', '?', '1' и т.н.),
    # кодът просто продължава към следващата итерация на цикъла,
    # като напълно игнорира символа, точно според условието.