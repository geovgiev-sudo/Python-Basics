# Четем входните данни (6 числа). Използваме float, защото условието позволява десетични числа.
x1 = float(input())  # x-координата на долния ляв ъгъл (или първата точка)
y1 = float(input())  # y-координата на долния ляв ъгъл (или първата точка)
x2 = float(input())  # x-координата на горния десен ъгъл (или втората точка)
y2 = float(input())  # y-координата на горния десен ъгъл (или втората точка)
x  = float(input())  # x-координатата на точката, която проверяваме
y  = float(input())  # y-координатата на точката, която проверяваме

# Проверяваме дали точката лежи на някоя от страните на правоъгълника.
# Условие 1: x съвпада с лявата или дясната граница (x1 или x2) И y е между y1 и y2 (включително).
# Условие 2: y съвпада с долната или горната граница (y1 или y2) И x е между x1 и x2 (включително).
on_border = ((x == x1 or x == x2) and (y1 <= y <= y2)) or ((y == y1 or y == y2) and (x1 <= x <= x2))

# Ако някое от двете условия е вярно, точката е на границата.
if on_border:
    print("Border")
else:
    print("Inside / Outside")
