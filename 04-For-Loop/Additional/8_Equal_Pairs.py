# Четем броя на двойките
n = int(input())

# Ще пазим сумата на предишната двойка, за да я сравняваме с текущата
previous_sum = 0

# Тук ще пазим най-голямата разлика между две последователни двойки
max_diff = 0

# Булева променлива, за да отбележим, че още не сме имали предишна двойка
first_pair = True

# Обхождаме всички двойки (всяка двойка се състои от 2 числа)
for i in range(n):
    # Четем първото число от двойката
    num1 = int(input())
    # Четем второто число от двойката
    num2 = int(input())

    # Изчисляваме сумата на текущата двойка
    current_sum = num1 + num2

    # Ако това е първата двойка, няма с какво да я сравним
    if first_pair:
        previous_sum = current_sum  # Запазваме сумата ѝ
        first_pair = False          # Вече имаме "предишна" двойка
    else:
        # Изчисляваме абсолютната разлика между текущата и предишната двойка
        diff = abs(current_sum - previous_sum)

        # Проверяваме дали тази разлика е най-голямата досега
        if diff > max_diff:
            max_diff = diff  # Ако е, я записваме като нов максимум

        # Обновяваме previous_sum за следващата итерация
        previous_sum = current_sum

# След като приключим с всички двойки:
if max_diff == 0:
    # Ако няма разлика между нито една двойка -> всички са еднакви
    print(f"Yes, value={previous_sum}")
else:
    # Ако има разлики -> отпечатваме най-голямата разлика
    print(f"No, maxdiff={max_diff}")
